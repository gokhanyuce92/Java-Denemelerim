    private void btn_dosyaindirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dosyaindirActionPerformed
        Timer timer = new Timer(50, null);
        
        timer.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                say++;
                
                checkprogress.setValue(say);
                
                if (checkprogress.getValue() == 100) {
                    //JOptionPane.showMessageDialog(DosyaIndirme.this, "Dosya İndirme Tamamlandı");
                    
                    timer.stop();
                    
                    SonucSayfasi sonucSayfasi = new SonucSayfasi();
                    
                    //setVisible(false);
                    //System.exit(0);
                    
                    setVisible(false);
                    sonucSayfasi.setVisible(true);
                }
            }
        });
        
        timer.start();
    }//GEN-LAST:event_btn_dosyaindirActionPerformed

